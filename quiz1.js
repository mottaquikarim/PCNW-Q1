/*
  @func avgTwoNumbers
  @param a {number}
  @param b {number}
  @returns {number}
  @desc - computes average of two numbers
          formular for average: a + b / 2
  @example avgTwoNumbers(1,2); // 1.5
*/
const avgTwoNumbers = (num1,num2) => (num1 + num2)/2;

console.log(avgTwoNumbers(1,2));
console.log(avgTwoNumbers(5,10));